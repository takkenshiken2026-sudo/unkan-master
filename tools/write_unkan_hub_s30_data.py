def cmp(
    slug: str,
    title: str,
    cat: str,
    tags: str,
    summary: str,
    labels: str,
    axes: list[tuple[str, list[str]]],
    article_title: str,
    lead: str,
    points: str,
    mistakes: str,
    tip: str,
    related: str,
    qa: list[tuple[str, str]],
) -> dict:
    return {
        "slug": slug,
        "title": title,
        "category": cat,
        "tags": tags,
        "summary": summary,
        "col_labels": labels,
        "compare_rows": _rows(*[{"axis": a, "cols": c} for a, c in axes]),
        "article_title": article_title,
        "article_lead": lead,
        "exam_points": points,
        "common_mistakes": mistakes,
        "memory_tip": tip,
        "related_terms": related,
        **_faq(qa),
    }


def num(
    slug: str,
    title: str,
    cat: str,
    tags: str,
    summary: str,
    highlight: str,
    items: list[tuple[str, str, str]],
    article_title: str,
    lead: str,
    points: str,
    mistakes: str,
    tip: str,
    related: str,
    qa: list[tuple[str, str]],
) -> dict:
    return {
        "slug": slug,
        "title": title,
        "category": cat,
        "tags": tags,
        "summary": summary,
        "highlight": highlight,
        "item_rows": _rows(*[{"item": i, "value": v, "note": n} for i, v, n in items]),
        "article_title": article_title,
        "article_lead": lead,
        "exam_points": points,
        "common_mistakes": mistakes,
        "memory_tip": tip,
        "related_terms": related,
        **_faq(qa),
    }


def mis(
    slug: str,
    title: str,
    cat: str,
    tags: str,
    summary: str,
    confusion: str,
    patterns: list[tuple[str, str, str, str]],
    article_title: str,
    lead: str,
    points: str,
    mistakes: str,
    tip: str,
    related: str,
    qa: list[tuple[str, str]],
) -> dict:
    return {
        "slug": slug,
        "title": title,
        "category": cat,
        "tags": tags,
        "summary": summary,
        "confusion_point": confusion,
        "pattern_rows": _rows(
            *[{"topic": t, "wrong": w, "correct": c, "trap": p} for t, w, c, p in patterns]
        ),
        "article_title": article_title,
        "article_lead": lead,
        "exam_points": points,
        "common_mistakes": mistakes,
        "memory_tip": tip,
        "related_terms": related,
        **_faq(qa),
    }


