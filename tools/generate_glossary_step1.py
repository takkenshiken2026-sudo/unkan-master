def resolve_related(all_terms: list[str], term: str, category: str, n: int = 3) -> str:
    """同一分野から関連用語を自動割当（シードに related 未指定時）。"""
    same = [t for t in all_terms if t != term]
    # prefer same category - we don't have cat map here easily, use index neighbors
    idx = all_terms.index(term) if term in all_terms else 0
    picks = []
    for off in (1, 2, 3, 4, 5):
        j = (idx + off) % len(all_terms)
        if all_terms[j] != term and all_terms[j] not in picks:
            picks.append(all_terms[j])
        if len(picks) >= n:
            break
    return ";".join(picks[:n])


